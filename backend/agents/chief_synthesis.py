def synthesize_report(
    note_parser_output: dict,
    temporal_output: dict,
    rag_output: dict,
    patient_info: dict,
    vitals_df: pd.DataFrame,
    labs_df: pd.DataFrame,
) -> dict:
    """Integrate all agent outputs into a final Diagnostic Risk Report.

    Parameters
    ----------
    note_parser_output : dict
        Output from ``note_parser.parse_all_notes`` or ``parse_clinical_note``.
    temporal_output : dict
        Should contain keys: timeline, scores, trends, disease_progression.
    rag_output : dict
        Should contain keys: recommendations, citations, evidence_level.
    patient_info : dict
        Patient demographics: patient_id, age, gender, admission_date, etc.
    vitals_df : DataFrame
        Columns: timestamp, parameter, value.
    labs_df : DataFrame
        Columns: timestamp, parameter, value.

    Returns
    -------
    dict – complete diagnostic risk report.
    """
    patient_id = patient_info.get("patient_id", "UNKNOWN")

    # Extract scores
    scores = temporal_output.get("scores", [])
    latest_sofa = {}
    latest_qsofa = {}
    if scores:
        latest = scores[-1]
        latest_sofa = {
            "total": latest.get("sofa_total", 0),
            "components": latest.get("sofa_components", {}),
        }
        latest_qsofa = {
            "total": latest.get("qsofa_total", 0),
            "components": latest.get("qsofa_components", {}),
        }

    # Extract trends
    trends = temporal_output.get("trends", [])
    timeline = temporal_output.get("timeline", [])
    disease_progression = temporal_output.get("disease_progression", "No progression data available.")

    # Detect lab outliers
    outlier_flags = detect_lab_outliers(labs_df)

    # ------------------------------------------------------------------
    # TWIST 2: Diagnosis Hold — detect contradictory results that clash
    # with 3 days of consistent data. If found, REFUSE to revise the
    # diagnosis based on the anomalous value. Filter out outlier-affected
    # labs from trend-based risk calculations.
    # ------------------------------------------------------------------
    diagnosis_hold = False
    diagnosis_hold_details: list[dict] = []
    outlier_lab_names = {o["lab_name"].lower() for o in outlier_flags}

    for outlier in outlier_flags:
        lab_name = outlier["lab_name"]
        z_score = outlier.get("z_score", 0)
        flagged_value = outlier.get("flagged_value")
        mean_val = outlier.get("mean", 0)

        # Check if this contradicts 3 days of consistent data
        consistency = _check_prior_consistency(labs_df, lab_name, window_days=3)

        if consistency["is_consistent"] and abs(z_score) >= OUTLIER_Z_THRESHOLD:
            diagnosis_hold = True
            diagnosis_hold_details.append({
                "lab_name": lab_name,
                "flagged_value": flagged_value,
                "z_score": z_score,
                "prior_mean": consistency["mean"],
                "prior_std": consistency["std"],
                "prior_count": consistency["count"],
                "prior_cv": consistency["cv"],
                "reason": (
                    f"{lab_name} value of {flagged_value} contradicts "
                    f"{consistency['count']} consistent readings over the prior "
                    f"72 hours (mean={consistency['mean']:.1f}, CV={consistency['cv']:.1%}). "
                    f"Z-score: {abs(z_score):.1f}. Probable mislabeled/erroneous result. "
                    f"Diagnosis NOT revised — awaiting confirmed redraw."
                ),
                "status": "AWAITING_REDRAW",
            })

    # Filter trends: exclude outlier-affected labs from risk calculation
    safe_trends = [
        t for t in trends
        if t.get("lab_name", "").lower() not in outlier_lab_names
    ]

    # ------------------------------------------------------------------
    # Data completeness check — detect when SOFA/qSOFA are artificially
    # low because critical lab parameters are missing (not genuinely normal).
    # ------------------------------------------------------------------
    sofa_total = latest_sofa.get("total", 0)
    qsofa_total = latest_qsofa.get("total", 0)
    sofa_components = latest_sofa.get("components", {})
    data_completeness = _assess_data_completeness(labs_df, vitals_df, sofa_components)

    # Determine overall risk level using SAFE trends only (outlier-excluded)
    risk_level = determine_risk_level(
        sofa_total, qsofa_total, safe_trends, len(outlier_flags),
        data_completeness=data_completeness,
        note_parser_output=note_parser_output,
        patient_info=patient_info,
    )

    # Build risk flags from conditions and evidence (using safe trends)
    risk_flags = _build_risk_flags(
        note_parser_output, temporal_output, rag_output,
        sofa_total, qsofa_total, safe_trends,
    )

    # If data is incomplete, add an explicit risk flag
    if data_completeness.get("is_incomplete", False):
        missing_list = data_completeness.get("missing_critical", [])
        risk_flags.insert(0, {
            "condition": "Insufficient Lab Data",
            "risk_level": "MODERATE",
            "evidence": [
                f"Missing critical labs: {', '.join(missing_list)}",
                f"Data completeness: {data_completeness.get('completeness_pct', 0):.0f}% of key parameters available",
                "SOFA score may underestimate severity due to missing organ-system data",
            ],
            "guideline_citations": [],
            "recommended_actions": [
                "Order missing lab panels immediately (CBC, BMP, LFTs, Lactate, Coagulation)",
                "Do NOT interpret low SOFA as 'stable' — data is insufficient for reliable scoring",
                "Perform bedside clinical assessment to supplement missing data",
            ],
            "confidence": 0.40,
        })

    # Generate executive summary
    summary = _generate_executive_summary(
        patient_info, risk_level, risk_flags, sofa_total, qsofa_total,
        disease_progression,
    )

    # If diagnosis is on hold, prepend hold notice to summary
    if diagnosis_hold:
        hold_labs = ", ".join(d["lab_name"] for d in diagnosis_hold_details)
        summary = (
            f"[DIAGNOSIS HOLD] Anomalous lab result(s) detected for: {hold_labs}. "
            f"These values contradict 72 hours of consistent data and are excluded "
            f"from the current assessment pending confirmed redraw. "
        ) + summary

    return {
        "patient_id": patient_id,
        "generated_at": datetime.now(),
        "summary": summary,
        "risk_level": risk_level,
        "risk_flags": risk_flags,
        "sofa_score": latest_sofa,
        "qsofa_score": latest_qsofa,
        "timeline": timeline,
        "outlier_flags": outlier_flags,
        "disease_progression": disease_progression,
        "diagnosis_hold": diagnosis_hold,
        "diagnosis_hold_details": diagnosis_hold_details,
        "data_completeness": data_completeness,
        "safety_caveat": _DIAGNOSIS_HOLD_CAVEAT if diagnosis_hold else _SAFETY_CAVEAT,
    }


def detect_lab_outliers(labs_df: pd.DataFrame) -> list[dict]:
    """Check each lab's latest value against its recent history for outliers.

    Parameters
    ----------
    labs_df : DataFrame
        Columns: timestamp, parameter, value.

    Returns
    -------
    list of dict – outlier flags with lab name and detection details.
    """
    if labs_df is None or labs_df.empty:
        return []

    # Determine column names — MIMIC uses "label" and "charttime", others may use "parameter" and "timestamp"
    param_col = "label" if "label" in labs_df.columns else "parameter" if "parameter" in labs_df.columns else None
    value_col = "valuenum" if "valuenum" in labs_df.columns else "value" if "value" in labs_df.columns else None
    time_col = "charttime" if "charttime" in labs_df.columns else "timestamp" if "timestamp" in labs_df.columns else None

    if param_col is None or value_col is None:
        return []

    outlier_flags: list[dict] = []
    parameters = labs_df[param_col].dropna().unique()

    for param in parameters:
        param_data = labs_df.loc[labs_df[param_col] == param].copy()
        param_data = param_data.dropna(subset=[value_col])
        if time_col and time_col in param_data.columns:
            param_data = param_data.sort_values(time_col)
        if len(param_data) < 3:
            continue

        values = param_data[value_col].astype(float).tolist()
        latest_value = values[-1]
        history = values[:-1]

        result = detect_outlier(history, latest_value, z_threshold=OUTLIER_Z_THRESHOLD)

        if result["is_outlier"]:
            alert_text = format_outlier_alert(
                {**result, "value": latest_value},
                param,
            )
            ts = param_data[time_col].iloc[-1] if time_col and time_col in param_data.columns else datetime.now()
            outlier_flags.append({
                "lab_name": param,
                "flagged_value": latest_value,
                "z_score": result["z_score"],
                "mean": result["mean"],
                "std": result["std"],
                "is_outlier": True,
                "alert": alert_text,
                "recommendation": result["recommendation"],
                "expected_range": f"{result['mean']:.1f} ± {result['std']:.1f}",
                "timestamp": ts,
            })

    return outlier_flags


def determine_risk_level(
    sofa_score: int,
    qsofa_score: int,
    trends: list[dict],
    outlier_count: int,
    *,
    data_completeness: Optional[dict] = None,
    note_parser_output: Optional[dict] = None,
    patient_info: Optional[dict] = None,
) -> str:
    """Determine the overall clinical risk level.

    Logic:
    - CRITICAL: SOFA >= 10, or SOFA >= 6 with rising lactate > 4
    - HIGH: SOFA >= 6, or qSOFA >= 2 with any concerning trend,
            or admission diagnosis is high-acuity with incomplete data
    - MODERATE: SOFA 2-5, or qSOFA >= 2, or any single concerning trend,
               or critical data is missing (preventing reliable scoring),
               or admission diagnosis suggests sepsis/organ failure
    - LOW: SOFA < 2 and qSOFA < 2 and no concerning trends and data is complete

    Parameters
    ----------
    sofa_score : int
    qsofa_score : int
    trends : list of dict
        Output from ``temporal_mapper.detect_all_trends``.
    outlier_count : int
        Number of detected lab outliers.
    data_completeness : dict, optional
        From ``_assess_data_completeness``.
    note_parser_output : dict, optional
        Output from Agent 1 — used to detect sepsis-related conditions.
    patient_info : dict, optional
        Patient demographics including admission diagnosis.

    Returns
    -------
    str – "LOW", "MODERATE", "HIGH", or "CRITICAL".
    """
    concerning_trends = [t for t in trends if t.get("is_concerning", False)]

    # Check for rising lactate > 4
    rising_lactate_critical = False
    for t in trends:
        if t.get("lab_name", "").lower() == "lactate" and t.get("trend") == "rising":
            values = t.get("values", [])
            if values:
                last_val = values[-1]
                if isinstance(last_val, dict):
                    last_val = last_val.get("value", 0)
                elif isinstance(last_val, (list, tuple)):
                    last_val = last_val[-1]
                if float(last_val) > 4.0:
                    rising_lactate_critical = True

    # CRITICAL
    if sofa_score >= 10:
        return "CRITICAL"
    if sofa_score >= 6 and rising_lactate_critical:
        return "CRITICAL"

    # HIGH
    if sofa_score >= 6:
        return "HIGH"
    if qsofa_score >= 2 and concerning_trends:
        return "HIGH"

    # MODERATE
    if 2 <= sofa_score <= 5:
        return "MODERATE"
    if qsofa_score >= 2:
        return "MODERATE"
    if concerning_trends:
        return "MODERATE"

    # ------------------------------------------------------------------
    # Safety net: if SOFA = 0 and data is INCOMPLETE, do NOT report LOW.
    # Missing data ≠ healthy. A sepsis patient with missing Creatinine,
    # Bilirubin, Lactate, Platelets will score SOFA=0 falsely.
    # ------------------------------------------------------------------
    is_data_incomplete = (
        data_completeness is not None
        and data_completeness.get("is_incomplete", False)
    )

    # Check if admission diagnosis or note-parsed conditions suggest
    # high-acuity pathology (sepsis, organ failure, shock, etc.)
    high_acuity_keywords = {
        "sepsis", "septic", "shock", "organ failure", "respiratory failure",
        "ards", "acute kidney", "aki", "pneumonia", "meningitis",
        "bacteremia", "endocarditis", "necrotizing", "peritonitis",
        "multi-organ", "mof", "dic",
    }

    has_high_acuity_diagnosis = False

    # Check admission diagnosis
    if patient_info:
        diag = str(patient_info.get("diagnosis", "")).lower()
        if any(kw in diag for kw in high_acuity_keywords):
            has_high_acuity_diagnosis = True

    # Check conditions extracted by Agent 1 (note parser)
    if not has_high_acuity_diagnosis and note_parser_output:
        aggregated = note_parser_output.get("aggregated", {})
        all_conditions = aggregated.get(
            "all_conditions",
            note_parser_output.get("all_conditions", []),
        )
        for cond in all_conditions:
            cond_str = cond.get("condition", cond) if isinstance(cond, dict) else str(cond)
            if any(kw in str(cond_str).lower() for kw in high_acuity_keywords):
                has_high_acuity_diagnosis = True
                break
        # Also check critical findings
        critical = aggregated.get(
            "critical_findings",
            note_parser_output.get("critical_findings", []),
        )
        for finding in critical:
            if any(kw in str(finding).lower() for kw in high_acuity_keywords):
                has_high_acuity_diagnosis = True
                break

    # If data is incomplete AND diagnosis suggests high-acuity → HIGH
    if is_data_incomplete and has_high_acuity_diagnosis:
        return "HIGH"

    # If data is incomplete (even without explicit high-acuity diagnosis) → MODERATE
    # Rationale: We cannot safely call a patient LOW risk if we lack the data to score them
    if is_data_incomplete:
        return "MODERATE"

    # If diagnosis is high-acuity but scores are genuinely zero with COMPLETE data → MODERATE
    # (The patient may be early in their course or improving)
    if has_high_acuity_diagnosis:
        return "MODERATE"

    # LOW — only when data is complete and scores are genuinely low
    return "LOW"


def generate_shift_handoff(
    report: dict,
    patient_info: dict,
    current_shift: str,
) -> dict:
    """Generate a shift handoff report from the diagnostic risk report.

    Parameters
    ----------
    report : dict
        Output from ``synthesize_report``.
    patient_info : dict
        Patient demographics and context.
    current_shift : str
        Current (outgoing) shift label, e.g. "Day", "Night", "Evening".

    Returns
    -------
    dict – structured shift handoff report.
    """
    patient_id = patient_info.get("patient_id", "UNKNOWN")

    # Determine incoming shift
    shift_sequence = {"Day": "Evening", "Evening": "Night", "Night": "Day"}
    incoming_shift = shift_sequence.get(current_shift, "Next")

    # Extract data from report
    risk_level = report.get("risk_level", "UNKNOWN")
    risk_flags = report.get("risk_flags", [])
    timeline = report.get("timeline", [])
    outlier_flags = report.get("outlier_flags", [])
    sofa_score = report.get("sofa_score", {})
    disease_progression = report.get("disease_progression", "")

    # Critical alerts
    critical_alerts: list[str] = []
    for flag in risk_flags:
        if flag.get("risk_level") in ("HIGH", "CRITICAL"):
            condition = flag.get("condition", "Unknown")
            evidence = flag.get("evidence", [])
            critical_alerts.append(
                f"{condition} ({flag['risk_level']}): {'; '.join(evidence[:3])}"
            )
    for outlier in outlier_flags:
        critical_alerts.append(outlier.get("alert", "Lab outlier detected"))

    # Recent changes (last 12 hours relative to latest timeline event, not wall clock)
    recent_changes: list[str] = []
    timeline_timestamps = [
        e["timestamp"] for e in timeline
        if isinstance(e.get("timestamp"), datetime)
    ]
    max_ts = max(timeline_timestamps) if timeline_timestamps else datetime.now()
    cutoff = max_ts - timedelta(hours=12)
    for event in timeline:
        ts = event.get("timestamp")
        if isinstance(ts, datetime) and ts >= cutoff:
            if event.get("severity") in ("warning", "critical") or event.get("event_type") == "alert":
                recent_changes.append(event.get("description", ""))

    # Pending actions from recommendations
    pending_actions: list[str] = []
    for flag in risk_flags:
        actions = flag.get("recommended_actions", [])
        pending_actions.extend(actions[:2])  # Top 2 per flag

    # Build SOFA trend string
    sofa_trend = _build_sofa_trend_string(report)

    # Medications/notes summary from risk flags
    medications_notes: list[str] = []
    for flag in risk_flags:
        evidence = flag.get("evidence", [])
        for e in evidence:
            if any(keyword in e.lower() for keyword in ["medication", "vasopressor", "antibiotic", "infusion"]):
                medications_notes.append(e)

    medications_notes_str = "; ".join(medications_notes) if medications_notes else "See medication administration record."

    # Generate natural language summary using Gemini
    patient_summary, active_problems = _generate_handoff_narrative(
        patient_info, risk_level, risk_flags, disease_progression, sofa_score,
    )

    return {
        "patient_id": patient_id,
        "generated_at": datetime.now(),
        "outgoing_shift": current_shift,
        "incoming_shift": incoming_shift,
        "patient_summary": patient_summary,
        "active_problems": active_problems,
        "critical_alerts": critical_alerts,
        "recent_changes": recent_changes,
        "pending_actions": pending_actions,
        "current_medications_notes": medications_notes_str,
        "risk_assessment": f"Overall risk: {risk_level}. {report.get('summary', '')}",
        "sofa_trend": sofa_trend,
        "safety_caveat": _HANDOFF_SAFETY_CAVEAT,
    }


